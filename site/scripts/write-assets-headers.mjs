#!/usr/bin/env node
/**
 * Post-OpenNext-build step: emit a `_headers` file into `.open-next/assets/`
 * so Cloudflare's Static Assets binding serves content-hashed `_next/static/*`
 * files with `Cache-Control: public, max-age=31536000, immutable`.
 *
 * Why:
 *   CF's Workers Static Assets binding serves matched files directly without
 *   invoking worker.ts — so any header logic in the worker never sees those
 *   responses and can't set cache headers. The default cache-control CF emits
 *   is `public, max-age=0, must-revalidate`, which forces browsers to
 *   re-validate every chunk on every navigation. For content-hashed
 *   filenames (everything under `_next/static/`), that's wasted roundtrips.
 *
 * Usage (called after `opennextjs-cloudflare build`):
 *   node scripts/write-assets-headers.mjs           # wv-site: basePath = /
 *   node scripts/write-assets-headers.mjs /sub/path # if ever moved under a basePath
 *
 * Mirrors the script in harbour-apps/packages/security/scripts/ but adapted
 * for wv-site's root mount — passing `/` or no arg both produce the
 * unprefixed `/_next/static/*` pattern. Re-running is idempotent.
 *
 * Reference: https://developers.cloudflare.com/workers/static-assets/headers/
 */

import { writeFileSync, existsSync } from "node:fs";
import { resolve, join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

// Normalise basePath: "" and "/" both mean the app is mounted at the root,
// in which case the _next path is just "/_next/static/*" with no prefix.
const raw = process.argv[2] ?? "";
const basePath = raw === "/" ? "" : raw;
if (basePath && !basePath.startsWith("/")) {
  console.error(
    "ERROR: basePath must start with / (e.g. /harbour/<app>) or be omitted for root",
  );
  process.exit(2);
}

// `cd` to the site/ root so this works whether called from the site/
// directory or from the repo root via an npm script.
const here = dirname(fileURLToPath(import.meta.url));
const siteRoot = resolve(here, "..");
const assetsDir = resolve(siteRoot, ".open-next/assets");
if (!existsSync(assetsDir)) {
  console.error(
    `ERROR: ${assetsDir} does not exist — run 'opennextjs-cloudflare build' first`,
  );
  process.exit(3);
}

const headersBody = `# Generated by site/scripts/write-assets-headers.mjs.
# Do not hand-edit — re-run the script after each opennextjs-cloudflare build.

${basePath}/_next/static/*
  Cache-Control: public, max-age=31536000, immutable
`;

const target = join(assetsDir, "_headers");
writeFileSync(target, headersBody, "utf8");
console.log(`✓ wrote ${target}`);
console.log(`  basePath: ${basePath || "/"}`);
